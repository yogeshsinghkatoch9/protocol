/** Map for O(1) lookup by ID */
const compoundsById = new Map(compounds.map(c => [c.id, c]));

/** Map for O(1) lookup by any alias, brand name, or compound name (case-insensitive) */
const compoundsByAlias = new Map();
compounds.forEach(c => {
  const keys = [
    c.name,
    c.id,
    ...(c.aliases || []),
    ...(c.brandNames || [])
  ].map(a => a.toLowerCase().trim());
  keys.forEach(k => compoundsByAlias.set(k, c));
});

/**
 * Find a compound by ID, name, brand name, or alias.
 * @param {string} query
 * @returns {object|undefined}
 */
function findCompound(query) {
  if (!query) return undefined;
  const q = query.toLowerCase().trim();
  return compoundsByAlias.get(q) || compoundsById.get(q);
}

/**
 * Return all compounds in a given category.
 * @param {string} category
 * @returns {object[]}
 */
function getByCategory(category) {
  return compounds.filter(c => c.category === category);
}

/**
 * Full-text fuzzy search across name, aliases, brand names, notes, class, and description.
 * Returns results sorted by relevance (exact matches first, then partial matches).
 * @param {string} term
 * @returns {object[]}
 */
function search(term) {
  if (!term) return [];
  const q = term.toLowerCase().trim();

  const scored = [];
  for (const c of compounds) {
    // Build searchable text
    const nameStr = c.name.toLowerCase();
    const aliasStr = (c.aliases || []).join(' ').toLowerCase();
    const brandStr = (c.brandNames || []).join(' ').toLowerCase();
    const classStr = (c.class || '').toLowerCase();
    const descStr = (c.description || '').toLowerCase();
    const notesStr = (c.notes || '').toLowerCase();
    const idStr = c.id.toLowerCase();
    const bestForStr = (c.bestFor || []).join(' ').toLowerCase();
    const categoryStr = (c.category || '').toLowerCase();

    // Exact match on id, name, alias, or brand = highest priority
    if (idStr === q || nameStr === q) {
      scored.push({ compound: c, score: 100 });
    } else if (aliasStr.split(' ').includes(q) || brandStr.split(' ').map(w => w.toLowerCase()).includes(q)) {
      scored.push({ compound: c, score: 90 });
    } else if (nameStr.includes(q) || idStr.includes(q)) {
      scored.push({ compound: c, score: 70 });
    } else if (aliasStr.includes(q) || brandStr.includes(q)) {
      scored.push({ compound: c, score: 60 });
    } else if (classStr.includes(q) || categoryStr.includes(q)) {
      scored.push({ compound: c, score: 40 });
    } else if (bestForStr.includes(q)) {
      scored.push({ compound: c, score: 30 });
    } else if (descStr.includes(q) || notesStr.includes(q)) {
      scored.push({ compound: c, score: 20 });
    }
  }

  // Sort by score descending, then alphabetically by name
  scored.sort((a, b) => b.score - a.score || a.compound.name.localeCompare(b.compound.name));
  return scored.map(s => s.compound);
}


